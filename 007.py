print('A soma é {},'.format(n1+n2),end=' ')
print('a multiplicação é {},'.format(n1*n2),end=' ')
print('a divisão é {:.3f},'.format(n1/n2),end=' ')
print('a divisão inteira é {},'.format(n1//n2),end=' ')
print('e a potencia é {}.'.format(n1**n2))
